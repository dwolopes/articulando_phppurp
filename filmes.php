            <h4 class ="section-heading" >Tem dificuldades em falar em público? Se sente inseguro diante de uma plateia? Gosta de filmes? Já pensou em aprender e se inspirar em uma sessão regada de pipoca?</h4> 

            <p>Nada melhor do que aprender se divertindo vendo um filme e comendo uma pipoca, não é mesmo?</p>

            <p>Pois nesse post você vai receber a dica de alguns filmes para assistir e se inspirar, e superar seus medos e sua insegurança ao falar em público.</p>

            <h4>Discurso do Rei - 2011 (Disponivel no Netflix)</h4>
            <a href="#">
              <img class="img-fluid" src="http://imageshack.com/a/img924/3203/Fvabgc.jpg" alt="">
            </a>


            <p>Conta a história do Rei Jorge VI (Colin Firth) que ainda como príncipe tinha muitas dificuldades de falar em público, por ter uma posição de destaque na monarquia Inglesa como segundo na linha de sucessão ao trono britânico ele por influência de sua mulher decide procurar a ajuda de Lionel Logue (Geoffrey Rush) um terapeuta e fonoaudiólogo. 
            Com um tratamento usando da leitura, dramaturgia teatral e música, Logue tem o objetivo de fortalecer alguns músculos, com dança e o relaxamento de outros, e uma busca constante do controle respiratório do príncipe ao mesmo tempo que procuram uma explicação psicológica para sua gagueira. 
            Ao decorrer do filme podemos ver a superação, dedicação para resolver o seu medo de falar em público. A história é um grande exemplo de que com dedicação e comprometimento este problema pode ser resolvido.</p>

            <h4>Sociedade dos Poetas mortos - 1990 (Disponivel no Netflix)</h4>
            <a href="#">
              <img class="img-fluid" src="http://www.conspiracast.com.br/wp-content/uploads/2014/10/poets.jpg" alt="">
            </a>


            <p>Conta a história do professor John Keating interpretado pelo genial Robin Williams, Keating é um professor diferenciado que pensa literalmente “fora da caixa” e chega para fazer a diferença para os Jovens da Academia Welton, e abala o conservadorismo escola.
            Com discursos criativos, emocionantes e desafiadores desperta nos alunos e em todos que assistirem a aumentarem sua autoestima e buscar os sonhos. A longo do filme os alunos se tornam mais participativos, envolvidos e deixam de ser apenas ouvintes.</p>

            <h4>JOBS - 2013 (Disponivel no Netflix)</h4>
            <a href="#">
              <img class="img-fluid" src="http://www.banburyshireinfo.co.uk/wp-content/uploads/Steve-Jobs-movie.jpg" alt="">
            </a>


            <p>Antes de falar deste filme queria agradecer ao senhor Steve Jobs pelo meu Iphone e pelo meu MacBook que uso neste momento. Hahaha.
            Jobs além de um grande empresário e inventor foi uma pessoa que sempre pensou além da curva com seu espírito inovador modificou tudo na história da informática, com seus projetos que tornaram a tecnologia um extensão do corpo humano. 
            Mas Steve Jobs não foi apenas isso, ele foi um grande orador e esteve à frente da Apple na maioria de seus lançamentos com apresentações envolventes e surpreendentes. Jobs dá uma aula de como devemos nos dedicar e nos preparar para nossas apresentações ao longo do filme.</p>

            <h4>Obrigado por fumar – 2006</h4>
            <a href="#">
              <img class="img-fluid" src="https://68.media.tumblr.com/bbccf2ac36fc9b3406abd86e37b3664f/tumblr_n5fub1BZvH1qbwxizo1_500.png" alt="">
            </a>


            <p>Este filme vai te ensinar como é necessário ter bons argumentos, o quanto é importante saber quais palavras usar para manipular as pessoas. 
            Ao decorrer deste filme que tem um nome um tanto quanto polêmico “Obrigado por fumar” é retratada a vida de Nick Naylor um lobista que é o porta-voz de uma empresa de tabaco. 
            Nick usa de sua habilidade persuasiva para executar os interesses da companhia que trabalha e consequentemente acaba influenciando outras companhias. 
            A eficácia dos argumentos de Nick faz com que ele controle a sociedade mesmo tratando de um assuntos polêmicos e não éticos.</p>

            <p>Agora é só preparar a pipoca, servir um copão do seu refrigerante favorito e começar a se inspirar nestes personagens. Boa sessão!</p>

