            <h4 class ="section-heading" >Você tem um bom Network? Tem o costume de ir em eventos empresariais? Cuida do seu perfil no LinkedIn? Salva as informações de seus contatos? </h4> 

            <p>Se todas suas respostas para as perguntas feitas tenham sido “sim”, ótimo! Neste artigo vou te dar mais algumas dicas para melhorar ainda mais seu Network.
            Mas se suas respostas foram “não” fique tranquilo que ao acabar de ler este artigo você vai entender a importância de ter uma lista de contatos eficaz e vai começar a virar o jogo.
            Vamos lá?</p>

            <h4><strong>A Importância</strong></h4>

            <p>Network é uma ferramenta muito importante nos dias atuais com o mercado aquecido e em constante mudança, torna-se necessário estar sempre em busca de conhecimento e aprendizado, ter a noção do quanto é importante a sua rede de contatos, e como ela é vital para o seu sucesso profissional.
            Segundo a Ernst & Young até 2025 um em cada três postos de trabalho devem ser substituídos por tecnologia inteligente. Essa previsão assusta, pois não estamos falando de um grande espaço de tempo como a viagem de McFly e Dr.Brown, estamos falando de daqui 8 anos.
            Para estar pronto para essas grandes mudanças, segue a dica: <strong>Tenha um bom network</strong>, mantenha sua lista de contatos sempre ativa e em constante crescimento.</p>

            <h4><strong>A Construção</strong></h4>

            <p>A construção de sua rede de <strong>Network</strong> deve acontecer de forma natural, nada de forçar uma relação. Devemos estar preocupados em ter algo para trocar, se não deixa de ser uma relação e vira um exploração. Devemos manter nossa rede ativa, fazendo um esforço para manter o determinado contato por perto, saber seus projetos, se disponibilizar para ajudar, e não apenas quando precisamos, pois essa atitude não pega nada bem.</p>

            <h4><strong>Redes Socias</strong></h4>

            <p>As Redes Sociais são ferramentas que permite nos conectar a pessoas de todo o mundo, de todas as áreas. Quando o assunto é <strong>Network</strong> é através delas (LinkedIN e Facebook) podemos acompanhar os projetos profissionais, as conquistas o dia-a-dia de nossa rede. Interaja com seus contatos de forma natural, demonstre real interesse não tente ser o que você não é, você não vai conseguir manter essa máscara por muito tempo.</p>

            <h4><strong>Aplicativos </strong></h4>
            
            <p>Existem alguns aplicativos que são ótimos para gerenciar sua rede de contatos, como os próprios aplicativos das redes sociais (LinkedIn, Facebook). Abuse da agenda para armazenar seus contatos e salvar seus os dados completos, tanto as digitais quanto as físicas, não se esqueça também que em muitos encontros empresariais ou ocasionais ainda é comum aquela troca de cartões, um pouco antiquada mas ainda presente, então não esqueça de providenciar o seu. Para este momento segue uma dica: existe um aplicativo que chama CamCard que com uma simples foto do cartão entregue  ele consegue copiar todas as informações existentes no cartão além de salvar a foto.</p>

            <ul>Agora conheça dicas para fazer um bom networking:</ul>
              <li><strong>Autenticidade</strong> – Seja sempre você mesmo e autêntico como profissional, só assim você criará uma rede de contatos forte e que ajudará você a construir sua rede de networking</li>
              <li><strong>Escute</strong> – Criar uma rede de networking não significa apenas em falar com empresas e profissionais, mas também é saber escutar. Portanto seja sempre cuidadoso, crie o hábito de escutar outros profissionais e quando possível auxiliar em algo também</li>
              <li><strong>Esteja disposto</strong> – Criar um networking significa criar relacionamento com as pessoas, mas esteja aberto para outras pessoas também criarem um relacionamento com você, troque experiências</li>
              <li><strong>>Esteja online</strong> – Hoje em dia fazer um bom networking se tornou ainda mais fácil. Utilize as redes sociais ao seu favor, esteja presente no mundo virtual, trocando informações, experiência e criando rede de contatos com perfis no Facebook, Instagram e Linkedin, por exemplo;</li>
              <li><strong>Utilize uma comunicação personalizada</strong> – Um dos segredos para fazer um bom networking é criar um planejamento antes de realizar qualquer contato. Procure criar uma maneira personalizada de realizar uma comunicação, seja presencialmente ou via online;</li>
              <li><strong>ão perca oportunidades</strong>– Claro que em alguns lugares e momentos não são feitos para criar um networking, mas aproveite as oportunidades, pode ser entre familiares, amigos, local de trabalho e eventos;</li>
              <li><strong>Tenha cartões</strong></li> – Sempre carregue junto de si cartões de visita, isto passa mais credibilidade profissional para os outros além de levar seu contato de forma marcada, isto é fazer um bom networking.


